#include<bits/stdc++.h>
using namespace std;
int main()
{
    int a=4;
    int b=6;

    // and
    cout<<" a&b = "<<(a&b)<< endl;
    // or
    cout<<" a|b = "<<(a|b)<< endl;
    // not
    cout<<" ~a = "<<~a<< endl;
    // xor
    cout<<" a^b = "<<(a^b)<< endl;

    cout<<(17>>1)<<endl;
    cout<<(17>>2)<<endl;
    cout<<(19<<1)<<endl;
    cout<<(21<<2)<<endl;


return 0;
}  